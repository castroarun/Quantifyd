class WhatsAppProvider:
    """Send WhatsApp messages via Twilio. Stub -- to be implemented."""

    def __init__(self, config):
        self.account_sid = config.get('twilio_account_sid', '')
        self.auth_token = config.get('twilio_auth_token', '')
        self.from_number = config.get('twilio_whatsapp_from', '')
        self.to_number = config.get('twilio_whatsapp_to', '')

    def send(self, notification):
        if not all([self.account_sid, self.auth_token, self.from_number, self.to_number]):
            return

        # Build plain text message for WhatsApp
        if notification['type'] == 'eod_report':
            text = self._build_eod_text(notification)
        else:
            text = f"{notification['title']}\n{notification['message']}"

        try:
            from twilio.rest import Client
            client = Client(self.account_sid, self.auth_token)
            client.messages.create(
                body=text,
                from_=self.from_number,
                to=self.to_number,
            )
            logger.info(f"[WhatsApp] Sent: {notification['title'][:50]}")
        except Exception as e:
            logger.error(f"[WhatsApp] Send failed: {e}")

    def _build_eod_text(self, notification):
        data = notification.get('data', {})
        trades = data.get('trades', [])
        day_pnl = sum(t.get('pnl_inr', 0) or 0 for t in trades)
        winners = len([t for t in trades if (t.get('pnl_inr') or 0) > 0])
        losers = len(trades) - winners

        lines = [
            f"\U0001f4ca *ORB Daily Report* -- {date.today().strftime('%d %b %Y')}",
            f"",
            f"Trades: {len(trades)} ({winners}W {losers}L)",
            f"Day P&L: {'+'if day_pnl>=0 else ''}Rs {day_pnl:,.0f}",
            f"Cumulative: {'+'if data.get('cumulative_pnl',0)>=0 else ''}Rs {data.get('cumulative_pnl',0):,.0f}",
            f"",
        ]
        for t in trades:
            pnl = t.get('pnl_inr', 0) or 0
            if t.get('exit_reason') == 'target':
                ticon = '\U0001f3af'
            elif t.get('exit_reason') == 'sl':
                ticon = '\U0001f534'
            else:
                ticon = '\u23f0'
            lines.append(f"{ticon} {t.get('instrument', '')} {t.get('direction', '').upper()} {'+'if pnl>=0 else ''}Rs {pnl:,.0f}")

        return '\n'.join(lines)


# --- Singleton ---

_instance = None
_lock = threading.Lock()


def get_notification_service(config=None):
    global _instance
    if _instance is None:
        with _lock:
            if _instance is None:
                if config is None:
                    from config import ORB_DEFAULTS
                    config = ORB_DEFAULTS
                _instance = NotificationService(config)
    return _instance
